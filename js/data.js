function chooseImg(fileInput) {
    if (fileInput.files && fileInput.files[0]) {
        var reader = new FileReader();

        reader.onload = function (e) {
            $('#image').attr('src', e.target.result);
        }
        reader.readAsDataURL(fileInput.files[0]);
    }
}
function addToFavorites() {
    // Retrieve the favorites from local storage or initialize an empty array
    var id = localStorage.getItem("id")
    var name = localStorage.getItem("name")
    var quocGia = localStorage.getItem("quocgia")
    var theLoai = localStorage.getItem("theloai")
    var tomTat = localStorage.getItem("tomtat")

    var item = localStorage.getItem("list-item") ? JSON.parse(localStorage.getItem("list-item")) : []
    var check = true
    for (var i = 0; i < item.length; i++) {
        if (item[i].name === name) {
            check = false;
            break;
        }
    }

    if (!check) {
        alert("Phim đã có trong danh sách")
    } else {
        item.push({
            id: id,
            name: name,
            quocGia: quocGia,
            theLoai: theLoai,
            tomTat: tomTat
        })
    }

    localStorage.setItem("list-item", JSON.stringify(item))


}

